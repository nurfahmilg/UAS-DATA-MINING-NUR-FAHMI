import pandas as pd
from sklearn.preprocessing import MinMaxScaler
data = pd.read_csv('Pizza_Restaurants_and_the_Pizza_They_Sell.csv')
data.head(50)

import numpy as np
import matplotlib.pyplot as plt
from sklearn.cluster import KMeans
from sklearn.preprocessing import MinMaxScaler

data_clus1 = data
data_clus1

from sklearn.preprocessing import LabelEncoder
data_clus1 = data_clus1.apply(LabelEncoder().fit_transform)
data_clus1.head(50)

data_clus = data.filter(["city","name"])
data_clus

from sklearn.preprocessing import LabelEncoder
data_clus = data_clus.apply(LabelEncoder().fit_transform)
data_clus

plt.scatter(data_clus.iloc[:,0],data_clus.iloc[:,1],c='red',s=10)
plt.xlabel('city')
plt.ylabel('name')
plt.show()

clus_array = np.array(data_clus)
print(clus_array)

scaler = MinMaxScaler()
data_scaled = scaler.fit_transform(clus_array)
data_scaled

distortions = []
K = range (1,10)
for k in K:
  kmeanModel = KMeans(n_clusters=k)
  kmeanModel.fit(data_scaled)
  distortions.append(kmeanModel.inertia_)

plt.figure(figsize=(10,6))
plt.plot(K, distortions, 'bx-')
plt.xlabel('k')
plt.ylabel('Distortion')
plt.title('The Elbow Method showing the optimal k')
plt.show()

kmeanModel = KMeans(n_clusters=4)
kmeanModel.fit(data_scaled)
# KMeans(algorithm='auto', copy_x=True, init='k-means++', max_iter=300,
#        n_clusters=5, n_init=10,n_jobs=None,precompute_distances='auto',
#        random_state=None, tol=0.0001, verbose=0)
print(kmeanModel.cluster_centers_)

plt.scatter(data_scaled[:,0], data_scaled[:,1], s=10, c='black', marker='o', alpha=1)
centers = kmeanModel.cluster_centers_
plt.scatter(centers[:,0], centers[:,1], c='red', s=50, alpha=1, marker='o');
plt.title('K-Means Centroid')
plt.xlabel('city')
plt.ylabel('name')
plt.show()

y_kmeans = kmeanModel.predict(data_scaled)

plt.scatter(data_scaled[:,0], data_scaled[:,1], c=y_kmeans, s=10, cmap='viridis')

centers = kmeanModel.cluster_centers_
plt.scatter(centers[:, 0], centers[:,1], c='red', s=200, alpha=0.5);
plt.xlabel('city')
plt.ylabel('name')
plt.show()

result = pd.DataFrame(data = y_kmeans)
result.columns = ['cluster']
result.head()

output = pd.concat([data_clus,result], axis=1)
output.head()

import seaborn as sns
sns.scatterplot(x=output.iloc[:,0], y=output.iloc[:,1], hue=output.iloc[:,2],
                data=output, palette='viridis', s=10, legend='full');
plt.legend(loc='center right', fontsize='10');

